void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if (huart->Instance == USART2) {
		HAL_UART_Receive_IT(&huart2, rcv_buff, 8);
		if( rcv_buff[0] == 0x15 && rcv_buff[1] == 0x04 && rcv_buff[2] == 0x02) {
			uint16_t new_co2 = (uint16_t)(rcv_buff[3] << 8 | rcv_buff[4]);
			if( new_co2 != co2 ) {
				co2 = new_co2;

				// Get the current time-stamp
				HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN);
				HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN);

				// Get the current temperature
				HAL_ADC_Start(&hadc1);
				HAL_ADC_PollForConversion(&hadc1, 1000);
				uint16_t raw = HAL_ADC_GetValue(&hadc1);

				// Create the string to send
				sprintf(snd_buff,"CO2: %04d | TT: %02d-%02d-%02d %02d.%02d.%02d | T: %d\r\n",
						co2, sDate.Year, sDate.Month, sDate.Date,
						sTime.Hours, sTime.Minutes, sTime.Seconds, raw);

				// Transmit the string
				HAL_UART_Transmit_IT(&huart3, snd_buff, sizeof(snd_buff));
			}
		}
	}
}
