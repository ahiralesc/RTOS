/* USER CODE BEGIN Header_toggleGreenHook */
/**
* @brief Function implementing the toggleGreen thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_toggleGreenHook */
void toggleGreenHook(void *argument)
{
  /* USER CODE BEGIN toggleGreenHook */

	/* The task ACTIVE/INACTIVE frequency length */
	TickType_t frequency = 6000;

	/* Get the current tick count */
	TickType_t tickCount = xTaskGetTickCount();

	for(;;)
	{
		/* Turn ON the green LED */
		HAL_GPIO_WritePin(EBlue_GPIO_Port, EBlue_Pin, GPIO_PIN_SET);

		/* Start timer 6 and initialize it to zero */
		HAL_TIM_Base_Start_IT(&htim6);
		 __HAL_TIM_SET_COUNTER(&htim6, 0);

		 /*
		  * The HCLK and APB1 timer runs at 16 MHz. Given
				 * TIM6 pre-scalar of 105
				 * TIM6 counter period of 65535
				 * The time length is of approximately 1s. Thus we need to count 4 times
				 * if vc1 has reached zero (interrupt). This is done in HAL_TIM_PeriodElapsedCallback.
				 */
		while(vc1 <= 3){
			HAL_GPIO_TogglePin(EGreen_GPIO_Port, EGreen_Pin);
			HAL_Delay(25);
		}

		/* Stop time 6 */
		HAL_TIM_Base_Stop_IT(&htim6);

		/* Toggle OFF green LED */
		HAL_GPIO_WritePin(EGreen_GPIO_Port, EGreen_Pin, GPIO_PIN_RESET);

		vc1 = 0;

		HAL_GPIO_WritePin(EBlue_GPIO_Port, EBlue_Pin, GPIO_PIN_RESET);
		vTaskDelayUntil( &tickCount, frequency );
	}
  /* USER CODE END toggleGreenHook */
}

/* USER CODE BEGIN Header_toggleRedHook */
/**
* @brief Function implementing the toggleRed thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_toggleRedHook */
void toggleRedHook(void *argument)
{
  /* USER CODE BEGIN toggleRedHook */
	/* The task ACTIVE/INACTIVE frequency length */
		TickType_t frequency = 4000;

		/* Get the current tick count */
		TickType_t tickCount = xTaskGetTickCount();

		for(;;)	{
			HAL_GPIO_WritePin(EYellow_GPIO_Port, EYellow_Pin, GPIO_PIN_SET);

			/* Start timer 6 and initialize it to zero */
			HAL_TIM_Base_Start_IT(&htim7);
			__HAL_TIM_SET_COUNTER(&htim7, 0);
			while(vc2 <= 1){
				/*
				 * To produce the toggling rate of 20Hz we must estimate the delay as follows
				 * Delay (ms) = 1000 / Frequency (Hz)
				 *
				 * Thus, given a frequency of 20Hz the corresponding delay is
				 * 	Delay (ms) = 1000 / 20 = 50 ms.
				 *
				 * 	However, measurements with the Kingst Logic Analyzer (A5016) produce
				 * 	- Frequency	: 10Hz
				 * 	- Period 	: 102ms
				 * 	- Duty cycle: Approx. 50%
				 *
				 *  Cutting in half the delay time fixes this problem (HAL_Delay(25))
				 *
				 * 	The measured values are now:
				 * 	- Frequency	: 20Hz
				 * 	- Period 	: 52ms
				 * 	- Duty cycle: Approx. 50%
				 */
				HAL_GPIO_TogglePin(ERed_GPIO_Port, ERed_Pin);
				HAL_Delay(25);
			}

			/* Stop time 7 */
			HAL_TIM_Base_Stop(&htim7);
			HAL_GPIO_WritePin(ERed_GPIO_Port, ERed_Pin, GPIO_PIN_RESET);

			vc2 = 0;

			HAL_GPIO_WritePin(EYellow_GPIO_Port, EYellow_Pin, GPIO_PIN_RESET);
			vTaskDelayUntil( &tickCount, frequency );
		}
  /* USER CODE END toggleRedHook */
}

