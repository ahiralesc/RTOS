/* USER CODE BEGIN 4 */
void processMorseCode(uint8_t* buffer)
{
	for(int i=0; i<BUFFER_LENGHT; i++){

		if(buffer[i] == '.'){
			HAL_GPIO_WritePin(IBlue_GPIO_Port, IBlue_Pin, GPIO_PIN_SET);
			HAL_Delay(DOT_LENGHT);
			HAL_GPIO_WritePin(IBlue_GPIO_Port, IBlue_Pin, GPIO_PIN_RESET);
		}

		if(buffer[i] == '-'){
			HAL_GPIO_WritePin(IBlue_GPIO_Port, IBlue_Pin, GPIO_PIN_SET);
			HAL_Delay(DASH_LENGHT);
			HAL_GPIO_WritePin(IBlue_GPIO_Port, IBlue_Pin, GPIO_PIN_RESET);
		}

		if(buffer[i] == '/'){
			HAL_GPIO_WritePin(IBlue_GPIO_Port, IBlue_Pin, GPIO_PIN_SET);
			HAL_Delay(SLASH_LENGHT);
			HAL_GPIO_WritePin(IBlue_GPIO_Port, IBlue_Pin, GPIO_PIN_RESET);
		}

		if(buffer[i] == ' '){
			HAL_Delay(SPACE_LENGHT);
		}

		HAL_Delay(SPACE_LENGHT);
	}
}
